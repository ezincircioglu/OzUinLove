import Foundation


class Member {
    let name: String
    let lastName: String
    let userName: String
    let age: Int
    
    init(name: String, lastName: String, userName: String, age: Int) {
        self.name = name
        self.lastName = lastName
        self.userName = userName
        self.age = age
        
    }

}